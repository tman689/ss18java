let input = parseInt(prompt("Mời bạn nhập một số từ 0->9"));
switch(input){
    case 0:
        document.writeln("Không");
        break;
    case 1:
        document.writeln("Một");
        break;
    case 2:
        document.writeln("Hai");
        break;
    case 3:
        document.writeln("Ba");
        break;
    case 4:
        document.writeln("Bốn");
        break;
    case 5:
        document.writeln("Năm");
        break;
    case 6:
        document.writeln("Sáu");
        break;
    case 7:
        document.writeln("Bảy");
        break;
    case 8:
        document.writeln("Tám");
        break;
    case 9:
        document.writeln("Chín");
        break;
    default:
        document.writeln("Không hợp lệ");
        break;
}
